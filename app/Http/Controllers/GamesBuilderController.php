    {
        $this->validate($request, [
            'title' => 'required',
            'body' => 'required',
            'cover_image' => 'image|nullable|max:1999'
        ]);

    
        // ...
        $gametitle = new GamesTitleController;
        $game = $gametitle->store($request,$game);
        $Category = new GamesCategoryController;
        $game = $Category->store($request,$game);
        $CoverImage = new GamesCOVERIMAGEController;
        $game = $CoverImage->store($request,$game);
        $Body = new GamesBodyController;
        $game = $Body->store($request,$game);
        $userid = new Games_User_id_Controller;
        $game = $userid->store($request,$game);

        return $game ;
   
